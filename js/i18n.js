let lang = 'pt';

// Resolve a preferência explícita ou detecta de navigator.language (2 letras).
function resolveLang(pref) {
  if (pref === 'en' || pref === 'es' || pref === 'pt') return pref;
  try {
    const n = (navigator.language || 'pt').slice(0, 2).toLowerCase();
    return I18N_DICT[n] ? n : 'pt';
  } catch (e) { return 'pt'; }
}
function setLang(pref) { lang = resolveLang(pref); }
function getLang() { return lang; }

// locale pra Intl/toLocaleString acompanhar o idioma escolhido.
function i18nLocale() { return lang === 'pt' ? 'pt-BR' : lang === 'es' ? 'es' : 'en'; }

// t('chave', { name: 'Bia', n: 3 }) — escolhe a língua, cai pro pt e depois pra
// própria chave se faltar, e interpola {name}/{n}.
function t(key, vars) {
  let s = (I18N_DICT[lang] && I18N_DICT[lang][key]) || I18N_DICT.pt[key] || key;
  if (vars) for (const k of Object.keys(vars)) s = s.split('{' + k + '}').join(String(vars[k]));
  return s;
}

// Aplica o dicionário aos elementos marcados (chame após trocar de idioma).
function applyI18n(root) {
  root = root || document;
  root.querySelectorAll('[data-i18n]').forEach((n) => { n.textContent = t(n.getAttribute('data-i18n')); });
  // [data-i18n-html] injeta innerHTML — SÓ com valores do próprio dicionário (nunca dado da rede).
  root.querySelectorAll('[data-i18n-html]').forEach((n) => { n.innerHTML = t(n.getAttribute('data-i18n-html')); });
  root.querySelectorAll('[data-i18n-ph]').forEach((n) => { n.setAttribute('placeholder', t(n.getAttribute('data-i18n-ph'))); });
  root.querySelectorAll('[data-i18n-aria]').forEach((n) => { n.setAttribute('aria-label', t(n.getAttribute('data-i18n-aria'))); });
  root.querySelectorAll('[data-i18n-title]').forEach((n) => { n.setAttribute('title', t(n.getAttribute('data-i18n-title'))); });
  try { document.documentElement.lang = i18nLocale(); } catch (e) { /* ignore */ }
}

// Escopo global: scripts clássicos (app.js) usam t/applyI18n direto; window.* pra
// acesso explícito; globalThis.I18N_DICT pra auditoria de paridade em Node.
